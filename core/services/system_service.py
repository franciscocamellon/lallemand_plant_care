import os
import datetime

from ..constants import DIRECTORY_STRUCTURE


class SystemService:

    def __init__(self):
        """
        Constructor for the SystemService class.
        """
        pass

    @staticmethod
    def createDirectoryStructure(path):

        full_paths = [os.path.join(path, directory) for directory in DIRECTORY_STRUCTURE]

        for full_path in full_paths:
            os.makedirs(full_path, exist_ok=True)

    @staticmethod
    def createDate():
        createDate = datetime.datetime.now()
        return createDate.strftime('%Y-%m-%d %H:%M:%S')

